import type { ProxyMode } from "../runtimes/interface.ts";

/**
 * Base docker-compose template for picoclaw: picoclaw + API Proxy.
 * ~75x lighter than OpenClaw (~20MB vs ~1.5GB per instance).
 */
export function picoClawComposeTemplate(name: string, port: number, proxyMode: ProxyMode = "shared"): string {
  const hasProxy = proxyMode !== "none";
  return `# Generated by claw-farm — ${name} (picoclaw)
services:
${hasProxy ? `  api-proxy:
    build: ./api-proxy
    expose:
      - "8080"
    environment:
      LLM_PROVIDER: \${LLM_PROVIDER:-gemini}
      GEMINI_API_KEY: \${GEMINI_API_KEY:-}
      ANTHROPIC_API_KEY: \${ANTHROPIC_API_KEY:-}
      OPENAI_API_KEY: \${OPENAI_API_KEY:-}
      OPENAI_COMPAT_BASE_URL: \${OPENAI_COMPAT_BASE_URL:-}
      AUDIT_LOG_PATH: /logs/api-proxy-audit.jsonl
      MAX_PROMPT_SIZE_MB: 5
      PII_MODE: redact
    volumes:
      - ./logs:/logs
    networks:
      - proxy-net
    read_only: true
    tmpfs:
      - /tmp:size=50M
    security_opt:
      - no-new-privileges:true
    cap_drop:
      - ALL
    deploy:
      resources:
        limits:
          memory: 256M
          cpus: "0.5"
    healthcheck:
      test: ["CMD", "python", "-c", "import urllib.request; urllib.request.urlopen('http://localhost:8080/health')"]
      interval: 5s
      timeout: 3s
      retries: 5
    restart: unless-stopped

` : ""}  picoclaw-gateway:
    image: ghcr.io/sipeed/picoclaw:latest
    ports:
      - "127.0.0.1:${port}:18790"
    volumes:
      - ./picoclaw:/root/.picoclaw
${hasProxy ? `    environment:
      PICOCLAW_PROXY_URL: http://api-proxy:8080
    networks:
      - proxy-net` : ""}
    read_only: true
    tmpfs:
      - /tmp:size=50M
    security_opt:
      - no-new-privileges:true
    cap_drop:
      - ALL
    deploy:
      resources:
        limits:
          memory: 128M
          cpus: "0.5"
    healthcheck:
      test: ["CMD-SHELL", "nc -z localhost 18790 || exit 1"]
      interval: 5s
      timeout: 3s
      retries: 5
      start_period: 10s
${hasProxy ? `    depends_on:
      api-proxy:
        condition: service_healthy` : ""}
    restart: unless-stopped
${hasProxy ? `
networks:
  proxy-net:` : ""}
`;
}

