/**
 * Per-instance docker-compose for picoclaw (proxyMode=shared).
 * Only picoclaw container — api-proxy is shared at project level.
 *
 * Security: Each instance gets its own bridge network that connects ONLY to the
 * shared api-proxy container. Instances cannot reach each other — hub-and-spoke
 * topology prevents cross-tenant network access.
 */
export function picoClawInstanceSharedProxyComposeTemplate(
  projectName: string,
  userId: string,
  port: number,
): string {
  const containerPrefix = `${projectName}-${userId}`;
  return `# Generated by claw-farm — ${projectName} instance: ${userId} (picoclaw, shared proxy)
services:
  picoclaw-gateway:
    container_name: ${containerPrefix}-picoclaw
    image: ghcr.io/sipeed/picoclaw:latest
    ports:
      - "127.0.0.1:${port}:18790"
    volumes:
      - ./picoclaw:/root/.picoclaw
    environment:
      PICOCLAW_PROXY_URL: http://${projectName}-api-proxy:8080
    networks:
      - instance-net
    read_only: true
    tmpfs:
      - /tmp:size=50M
    security_opt:
      - no-new-privileges:true
    cap_drop:
      - ALL
    deploy:
      resources:
        limits:
          memory: 128M
          cpus: "0.5"
    healthcheck:
      test: ["CMD-SHELL", "nc -z localhost 18790 || exit 1"]
      interval: 5s
      timeout: 3s
      retries: 5
      start_period: 10s
    restart: unless-stopped

networks:
  instance-net:
    # Per-instance isolated network. Docker Compose prefixes with project name,
    # so the actual network is "${projectName}-${userId}_instance-net".
    # The shared api-proxy is connected dynamically via "docker network connect".
    # Each instance has its own network — no cross-tenant access possible.
`;
}

